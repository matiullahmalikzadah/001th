package Nabi;

public class IfElseIf {
    public static void main(String[] args) {
        int day=4;
        if (day==1){
            System.out.println("Monday");
        }else if (day==2){
            System.out.println("tuesday");
        } else if (day==3) {
            System.out.println("wednesday");
        } else if (day==4) {
            System.out.println("thursday");
        }
    }
}
